TEST(HashSetTest, SimpleInsert) {
  std::HashSet<int> hash_set;
  for (int i = 0; i < 10; i++) {
    hash_set.insert(i);
  }

  for (int i = 0; i < 10; i++) {
    EXPECT_TRUE(*hash_set.find(i) == i);
  }
  for (int i = 10; i < 100; i++) {
    EXPECT_TRUE(hash_set.find(i) == nullptr);
  }
}

TEST(HashSetTest, SimpleInsertAndErase) {
  std::HashSet<int> hash_set;
  for (int i = 0; i < 10; i++) {
    hash_set.insert(i);
  }

  for (int i = 0; i < 10; i += 2) {
    EXPECT_TRUE(hash_set.erase(i) == 1);
  }

  for (int i = 1; i < 10; i += 2) {
    EXPECT_TRUE(*hash_set.find(i) == i);
  }

  for (int i = 0; i < 10; i += 2) {
    EXPECT_TRUE(hash_set.find(i) == nullptr);
  }
}

TEST(HashSetTest, Rehash) {
  std::HashSet<int> hash_set;
  for (int i = 0; i < 10000; i++) {
    hash_set.insert(i);
  }

  for (int i = 0; i < 10000; i++) {
    EXPECT_TRUE(*hash_set.find(i) == i);
  }
  for (int i = 10000; i < 20000; i++) {
    EXPECT_TRUE(hash_set.find(i) == nullptr);
  }
}

void SanityCheck(const std::HashSet<int>& s, bool* is_in, size_t num) {
  for (size_t i = 0; i < num; i++) {
    if (is_in[i]) {
      EXPECT_EQ(*s.find(i), (int)i);
    } else {
      EXPECT_EQ(s.find(i), nullptr);
    }
  }
}

TEST(HashSetTest, RandomInsertAndRemoveTestSmall) {
  int data[] = {9, 3, 1, 3, 1, 4, 6, 2, 7, 7, 2, 5, 0, 5, 4, 8, 6, 0, 9, 8};
  bool is_in[10] = {0};

  std::HashSet<int> s;
  for (int i = 0; i < 20; i++) {
    if (is_in[data[i]]) {
      is_in[data[i]] = false;
      s.erase(data[i]);

      SanityCheck(s, is_in, 10);
    } else {
      is_in[data[i]] = true;
      s.insert(data[i]);
      SanityCheck(s, is_in, 10);
    }
  }
}

